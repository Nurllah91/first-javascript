var students = ["fahad", "tawrat", "Sanju",]

console.log(students);

// use push to add element to an array as the last element array 
students.push("Sajid");
students.push("mahi");
console.log(students);


// use pop to get out last element
students.pop();
console.log(students);

// var exStudent = students.pop();
